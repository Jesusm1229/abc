    private void jNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jNextActionPerformed
        Dialogs d; 
        // Se verifica si el rango de edades fue introducido correctamente.
        if(jComboEdadmin.getItemAt(jComboEdadmin.getSelectedIndex()) > jComboEdadmax.getItemAt(jComboEdadmax.getSelectedIndex())){
            {d = new Dialogs(8); //Inconsistencia en rango de edad
             d.setVisible(true);}
            return;
        }
            
        if(jTextEmpresa.getText().equals("") || jTextContEmpresa.getText().equals("") || !(jRadioMasc.isSelected() || jRadioFemen.isSelected() || jRadioAmbos.isSelected()) || !(domNacional.isSelected() || domInternacional.isSelected() || ambosDomicilio.isSelected()) || !(modoPresencial.isSelected() || modoRemoto.isSelected() || modoAmbos.isSelected()) || jTextCorreo.getText().equals("") || descripcionTrabajo.getText().equals("")){
            {d = new Dialogs(6); //FORMULARIO INCOMPLETO
             d.setVisible(true);}
            return;
        }
        
        if(agregarOfertaEmpleoDatabase(sexo, domicilio, modo, jTextContEmpresa.getText())){
            Formulario_Empleo_Conocimientos fec = new Formulario_Empleo_Conocimientos(Integer.parseInt(jTextContEmpresa.getText()), connection);
            fec.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jNextActionPerformed

