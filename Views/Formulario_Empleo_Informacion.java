    // Metodo para cargar todos los paises provenientes de la base de datos a los boxes de la vista.
    private void cargarPaises(){
    
            try{
                ResultSet rs = connection.prepareStatement("SELECT * FROM paises").executeQuery();
                
                while(rs.next())
                    jComboNacionalidad.addItem(rs.getString(3));
                
            } catch (SQLException ex) {
                Logger.getLogger(Administracion_json.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            jComboNacionalidad.setSelectedIndex(231);
    }
    
