    // Funcion para agregar un nuevo empleo a la base de datos.
    public boolean agregarOfertaEmpleoDatabase(String sexo, String domicilio, String modo, String codContrato){
        Dialogs d;
         try {
                PreparedStatement ps = connection.prepareStatement("INSERT INTO `oferta_empleo` (`idoferta_empleo`, `empresa` , `codcontrato`, `sexo`, `edadmin`, `edadmax`, `domicilio`, `modo`, `nacionalidad`, `descripcion`, `correo`) VALUES (NULL, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)");
                
                ps.setString(1, jTextEmpresa.getText());
                ps.setInt(2, Integer.parseInt(codContrato));
                ps.setString(3, sexo);
                ps.setInt(4, Integer.parseInt(jComboEdadmin.getSelectedItem().toString()));
                ps.setInt(5, Integer.parseInt(jComboEdadmax.getSelectedItem().toString()));
                ps.setString(6, domicilio);
                ps.setString(7, modo);
                ps.setString(8, jComboNacionalidad.getSelectedItem().toString());
                ps.setString(9, descripcionTrabajo.getText());
                ps.setString(10, jTextCorreo.getText());
                
                 // Verificamos si el codigo de contrato introducido ya se encuentra registrado.
                ResultSet rs = connection.prepareStatement("SELECT * FROM oferta_empleo").executeQuery();
                
                while(rs.next())
                    if(rs.getInt(3) == Integer.parseInt(codContrato)){
                       d = new Dialogs(11);//EMPLEO YA REGISTRADO
                       d.setVisible(true); 
                        return false;
                    }
                
                ps.executeUpdate();
                d = new Dialogs(9);//Oferta agregada correctamente
                d.setVisible(true);
                
            } catch (SQLException ex) {
                Logger.getLogger(Administracion_json.class.getName()).log(Level.SEVERE, null, ex);
                d = new Dialogs(5);
                d.setVisible(true);//ERROR GENERAL
            }
         return true;
    }
    
