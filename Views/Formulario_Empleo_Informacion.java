    private void jTextEmpresaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextEmpresaKeyTyped
        
        int character = (int)evt.getKeyChar();
        
        if(character < 65 || (character > 90 && character < 97) || character > 122 || (jTextEmpresa.getText().length() + 1 > 50) ){
            evt.consume();
            return;
        }
        
        char c=evt.getKeyChar();
        String s="";

        if(evt.getSource().equals(jTextEmpresa)) {
            if(Character.isDigit(c)||Character.isWhitespace(c)) {
                getToolkit().beep();
                evt.consume();
                s=jTextEmpresa.getText();
                jTextEmpresa.setText(null);
                jTextEmpresa.setText(s);
            }
        }
    }//GEN-LAST:event_jTextEmpresaKeyTyped

