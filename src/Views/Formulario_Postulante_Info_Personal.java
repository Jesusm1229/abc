    // Metodo de evento para establecer una limitacion de los caracteres que seran introducidos en los campos de texto (Numeros o Letras).
    private void jTextNOMBREKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextNOMBREKeyTyped
        
        int character = (int)evt.getKeyChar();
        
        if(character < 65 || (character > 90 && character < 97) || character > 122 || (jTextNOMBRE.getText().length() + 1 > 20)){
            evt.consume();
            return;
        }
        
        char c=evt.getKeyChar();
            String s="";
            
                if(evt.getSource().equals(jTextNOMBRE)) {
                    if(Character.isDigit(c)||Character.isWhitespace(c)) {
                        getToolkit().beep(); 
                        evt.consume();
                        s=jTextNOMBRE.getText();
                        jTextNOMBRE.setText(null);
                        jTextNOMBRE.setText(s);
                    }
                }
    }//GEN-LAST:event_jTextNOMBREKeyTyped

