     // Metodo para agregar un postulante a la base de datos.
    public boolean agregarPostulanteDatabase(){
            Dialogs d;
        
            try {
                PreparedStatement ps = connection.prepareStatement("INSERT INTO `postulantes` (`idpostulante`, `primernombre`, `segundonombre`, `primerapellido`, `segundoapellido`, `dni`, `edad`, `sexo`, `pais_origen`, `residencia`, `telefono`, `correo`) VALUES (NULL, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)");
                
                ps.setString(1, nuevo_postulante.getPrimerNombre());
                ps.setString(2, nuevo_postulante.getSegundoNombre());
                ps.setString(3, nuevo_postulante.getPrimerApellido());
                ps.setString(4, nuevo_postulante.getSegundoApellido());
                ps.setInt(5, nuevo_postulante.getDNI());
                ps.setInt(6, nuevo_postulante.getEdad());
                ps.setString(7, nuevo_postulante.getSexo());
                ps.setString(8, nuevo_postulante.getPais_origen());
                ps.setString(9, nuevo_postulante.getResidencia());
                ps.setString(10, nuevo_postulante.getTelefono());
                ps.setString(11, nuevo_postulante.getCorreo());
                
                // Verificamos si el DNI introducido o el correo ya se encuentran registrados.
                ResultSet rs = connection.prepareStatement("SELECT * FROM postulantes").executeQuery();
                
                while(rs.next())
                    if(rs.getInt(6) == nuevo_postulante.getDNI() || rs.getString(12).equalsIgnoreCase(nuevo_postulante.getCorreo())){
                        d = new Dialogs(3);//Ya Registrado
                        d.setVisible(true);
                        return false;
                    }
                
                ps.executeUpdate();
                d = new Dialogs(4);//CORRECTO
                d.setVisible(true);
                
            } catch (SQLException ex) {
                Logger.getLogger(Administracion_json.class.getName()).log(Level.SEVERE, null, ex);
                d = new Dialogs(5);
                d.setVisible(true);//ERROR GENERAL
            }
            
        return true;
    }

    //Método para cargar paises de la BBD al combobox
    private void cargarPaises(){
                
            try{
                ResultSet rs = connection.prepareStatement("SELECT * FROM paises").executeQuery();
                
                while(rs.next()){
                    jComboPais.addItem(rs.getString(3));
                    jComboResidencia.addItem(rs.getString(3));
                }
            } catch (SQLException ex) {
                Logger.getLogger(Administracion_json.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            jComboPais.setSelectedIndex(231);
            jComboResidencia.setSelectedIndex(231);
        }
    
